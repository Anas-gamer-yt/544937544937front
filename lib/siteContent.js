export function resolveSiteContent(source) {
  const rawContent =
    source && typeof source === "object" && "content" in source
      ? source.content
      : source;
  const safeContent =
    rawContent && typeof rawContent === "object" && !Array.isArray(rawContent)
      ? rawContent
      : {};

  return Object.fromEntries(
    Object.entries(DEFAULT_SITE_CONTENT).map(([key, defaultValue]) => [
      key,
      safeContent[key] === undefined || safeContent[key] === null
        ? defaultValue
        : String(safeContent[key])
    ])
  );
}
